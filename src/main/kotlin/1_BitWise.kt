class BitWise {
    fun bitWiseOperations() {
        val a = 11
        val b = 5

        println("bitWise and operation ${a and b}")
        println("bitWise or operation ${a or b}")
        println("bitWise xor operation ${a xor b}")
        println("bitWise shift left operation ${b shl 1}")
        println("bitWise shift right operation ${b shr 1}")
    }
}